package com.greenfox.arrayhandler;

public class Input {
  String what;
  int[] numbers;

  public void setWhat(String what) {
    this.what = what;
  }

  public void setNumbers(int[] numbers) {
    this.numbers = numbers;
  }

  public String getWhat() {
    return what;
  }

  public int[] getNumbers() {
    return numbers;
  }
}
